package chapter_8;

// Importo de forma estática a todos los métodos de la clase Math del paquete java.lang
import static java.lang.Math.*;

public class LaClaseMath {

    public static void main(String[] args) {

        //
        System.out.println("Métodos de la clase Math:");
        System.out.printf("sqrt(250) = %.1f%n", sqrt(250));
        System.out.printf("ceil(-5.8) = %.1f%n", ceil(-5.8));
        System.out.printf("E = %f%n", E);
        System.out.printf("PI = %f", PI);

    }

}
